uint32_t rtc_seconds(void) {
    outb(0x70, 0x00);
    uint32_t second = inb(0x71);
    return (second & 0x0F) + ((second / 16) * 10);
}

uint32_t rtc_minutes(void) {
    outb(0x70, 0x02);
    uint32_t minute = inb(0x71);
    return (minute & 0x0F) + ((minute / 16) * 10);
}

uint32_t rtc_hours(void) {
    outb(0x70, 0x04);
    uint32_t hour = inb(0x71);
    return (hour & 0x0F) + ((hour / 16) * 10);
}

void kernel_main(uint32_t magic, uint32_t addr) {
    if (magic != MULTIBOOT_MAGIC) {
        return;
    }
    multiboot_info_ptr = (multiboot_info_t*)addr;

    gdt_init();
    idt_init();
    pic_init();
    pit_init(100); // 100 Hz

    uint32_t end_of_kernel = (uint32_t)&_kernel_end;
    // Align to page
    if (end_of_kernel & 0xFFF) {
        end_of_kernel &= 0xFFFFF000;
        end_of_kernel += 0x1000;
    }

    // Heap size 32MB
    heap_init(end_of_kernel, 32 * 1024 * 1024);

    fb_init();
    keyboard_init();
    mouse_init();

    mouse_set_bounds(fb_get_width(), fb_get_height());

    fs_init();
    pcnet_init();

    login_screen();

    gui_init();

    net_init();
    dhcp_discover();

    asm volatile("sti");

    gui_run();

    while(1) {
        asm volatile("hlt");
    }
}
